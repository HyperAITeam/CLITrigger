const I18nContext = createContext<I18nContextType | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => {
    const saved = localStorage.getItem('clitrigger-lang');
    return (saved === 'ko' || saved === 'en') ? saved : 'ko';
  });

  const toggleLang = useCallback(() => {
    setLang((prev) => {
      const next = prev === 'en' ? 'ko' : 'en';
      localStorage.setItem('clitrigger-lang', next);
      return next;
    });
  }, []);

  const pluginT = useMemo(() => getPluginTranslations(), []);

  const t = useCallback(
    (key: string): string => {
      return (translations[lang] as Record<string, string>)[key] ?? pluginT[lang][key] ?? key;
    },
    [lang, pluginT]
  );

  return (
    <I18nContext.Provider value={{ lang, toggleLang, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be used within I18nProvider');
  return ctx;
}
